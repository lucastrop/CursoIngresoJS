/*
al presionar el botón mostrar 10 repeticiones 
con números ASCENDENTE, desde el 1 al 10.*/
function mostrar()
{
	let i = 0; //declaracion de iniciolizacion de la variable de control

	while(i<10){ //variable de control (condicion)
		i++ // es i=i+1
		alert(i);
//modificacion de la variable de control
	}
}//FIN DE LA FUNCIÓN